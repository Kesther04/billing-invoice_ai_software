// ─── Password Reset ───────────────────────────────────────────────────────────

export async function createPasswordResetToken(data: {
  userId:         string;
  organizationId: string;
  token:          string;
  expiresAt:      Date;
}): Promise<PasswordResetToken> {
  // Invalidate any live tokens for this user before issuing a new one
  await prisma.passwordResetToken.updateMany({
    where: { userId: data.userId, usedAt: null },
    data:  { usedAt: new Date() },
  });

  return prisma.passwordResetToken.create({ data });
}

export async function findValidResetToken(token: string): Promise<PasswordResetToken | null> {
  return prisma.passwordResetToken.findFirst({
    where: {
      token,
      usedAt:    null,
      expiresAt: { gt: new Date() },
    },
  });
}

export async function markResetTokenUsed(tokenId: string): Promise<void> {
  await prisma.passwordResetToken.update({
    where: { id: tokenId },
    data:  { usedAt: new Date() },
  });
}

export async function updateUserPassword(userId: string, passwordHash: string): Promise<void> {
  await prisma.user.update({
    where: { id: userId },
    data:  { password: passwordHash },
  });
}