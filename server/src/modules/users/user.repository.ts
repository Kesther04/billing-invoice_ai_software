// ─── Registration — Individual ────────────────────────────────────────────────
// Creates a personal INDIVIDUAL org + OWNER user atomically.

export async function createIndividualWithOrg(data: {
  userName:     string;
  email:        string;
  passwordHash: string;
}): Promise<{ user: User; org: Organization }> {
  return prisma.$transaction(async (tx) => {
    // The org email mirrors the user email for an individual account
    const org = await tx.organization.create({
      data: {
        name:  data.userName,          // org name = user's name for individuals
        email: data.email.toLowerCase().trim(),
        type:  "INDIVIDUAL" as OrganizationType,
        inviteCode: null,              // no invite code for individual orgs
      },
    });

    const user = await tx.user.create({
      data: {
        name:           data.userName,
        email:          data.email.toLowerCase().trim(),
        password:       data.passwordHash,
        role:           "OWNER" as UserRole,
        organizationId: org.id,
      },
    });

    return { user, org };
  });
}

